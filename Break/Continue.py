
# Break

print("--------Break Statement in Loop------------")

for i in range(10):
    if (i == 5):
        break
    print(i)
print("Rest of the Code")


print("------------------------------------------------------------------")

print("--------Continue Statement in Loop------------")

for i in range(10):
    if(i==5):
        continue
    print(i)
print("Rest of the Code")

