
# in --> Find element in specified sequence

str1 ="Welcome to Python"
print('P' in str1)  # True
print('z' in str1)  # False

# not in

str2 ="Welcome to Java"
print('J' not in str2)  # False
print('z' not in str2)  # True

str3 ="Welcome to Google"
print("subs" not in str3)

str4 ="Welcome top Bing"
print("to" in str4)

